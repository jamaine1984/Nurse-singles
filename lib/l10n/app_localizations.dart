  static String translate(String key, Locale locale) {
    final code = AppLanguages.normalizeFullySupportedCode(locale.languageCode);
    final translations = _translations[code] ?? _translations['en']!;
    return translations[key] ?? _translations['en']?[key] ?? _humanizeKey(key);
  }

  static String format(String key, Locale locale, Map<String, Object?> values) {
    var text = translate(key, locale);
    for (final entry in values.entries) {
      text = text.replaceAll('{${entry.key}}', '${entry.value}');
    }
    return text;
  }

  static String healthcareCredentialLabel(
    String? credentialType,
    Locale locale, {
    String? fallback,
  }) {
    final key = switch (credentialType) {
      'healthcareWorker' => 'credential_healthcare_worker',
      'nursingStudent' => 'credential_nursing_student',
      'travelNurse' => 'credential_travel_nurse',
      'agencyPartner' => 'credential_agency_partner',
      'collegePartner' => 'credential_college_partner',
      _ => null,
    };
    if (key == null) {
      return (fallback == null || fallback.isEmpty)
          ? translate('credential_healthcare_worker', locale)
          : fallback;
    }
    return translate(key, locale);
  }

